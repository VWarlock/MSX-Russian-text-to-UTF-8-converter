		52) utf="e294ac"	;; # 152 граница лёгкая вниз и горизонтально U+252C
		53) utf="e294a4"	;; # 153 граница лёгкая вертикальная и налево U+2524
		54) utf="e2949c"	;; # 154 граница лёгкая вертикальная и направо U+251C
