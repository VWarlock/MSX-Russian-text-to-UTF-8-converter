		5d) utf="e295b1"	;; # 15D граница легкая диагональ с верхнего правого в нижний левый U+2571
		5e) utf="e295b2"	;; # 15E граница легкая диагональ с верхнего левого в нижний правый U+2572
#		5f) utf=""		;; # 15F
	    esac
	    p=0
	    elif [ $dec -lt 128 ]; then
		utf="$char"
	    elif [ $dec -ge 128 ]; then
	    case $char in
		80) utf="e29681"	;; # 80 Нижняя одна восьмая блока U+2581
		81) utf="e2969a" 	;; # 81 Квадрант сверху слева и снизу справа U+259A
		82) utf="e29687" 	;; # 82 Нижние семь восьмых блока U+2587
		83) utf="e29694"	;; # 83 Верхняя одна восьмая блока U+2594
		84) utf="e28183"	;; # 84 Дефис маркер списка U+2043
#		85) utf=""		;; # 85
		86) utf="e2968f"	;; # 86 Левая одна восьмая блока U+258F
		87) utf="e2969e"	;; # 87 Квадрант сверху справа и снизу слева U+259E
#		88) utf=""		;; # 88
		89) utf="e29695"	;; # 89 Правая одна восьмая блока U+2595
