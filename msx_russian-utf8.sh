		d0) utf="d0bf"	;; # п U+043F
		d1) utf="d18f"	;; # я U+044F
		d2) utf="d180"	;; # р U+0440
		d3) utf="d181"	;; # с U+0441
		d4) utf="d182"	;; # т U+0442
		d5) utf="d183"	;; # у U+0443
		d6) utf="d0B6"	;; # ж U+0436
		d7) utf="d0b2"	;; # в U+0432
		d8) utf="d18c"	;; # ь U+044C
		d9) utf="d18b"	;; # ы U+044B
		da) utf="d0b7"	;; # з U+0437
		db) utf="d188"	;; # ш U+0448
		dc) utf="d18d"	;; # э U+044D
		dd) utf="d189"	;; # щ U+0449
		de) utf="d187"	;; # ч U+0447
		df) utf="d18a"	;; # ъ U+044A
		e0) utf="d0ae"	;; # Ю U+042E
		e1) utf="d090"	;; # А U+0410
		e2) utf="d091"	;; # Б U+0411
		e3) utf="d0a6"	;; # Ц U+0426
		e4) utf="d094"	;; # Д U+0414
		e5) utf="d095"	;; # Е U+0415
		e6) utf="d0a4"	;; # Ф U+0424
		e7) utf="d093"	;; # Г U+0413
		e8) utf="d0a5"	;; # Х U+0425
		e9) utf="d098"	;; # И U+0418
		ea) utf="d099"	;; # Й U+0419
		eb) utf="d09a"	;; # К U+041A
		ec) utf="d09b"	;; # Л U+041B
		ed) utf="d09c"	;; # М U+041C
		ee) utf="d09d"	;; # Н U+041D
		ef) utf="d09e"	;; # О U+041E
		f0) utf="d09f"	;; # П U+041F
		f1) utf="d0af"	;; # Я U+042F
		f2) utf="d0a0"	;; # Р U+0420
		f3) utf="d0a1"	;; # С U+0421
		f4) utf="d0a2"	;; # Т U+0422
		f5) utf="d0a3"	;; # У U+0423
		f6) utf="d096"	;; # Ж U+0416
		f7) utf="d092"	;; # В U+0412
		f8) utf="d0ac"	;; # Ь U+042C
		f9) utf="d0ab"	;; # Ы U+042B
		fa) utf="d097"	;; # З U+0417
		fb) utf="d0a8"	;; # Ш U+0428
		fc) utf="d0ad"	;; # Э U+042D
		fd) utf="d0a9"	;; # Щ U+0429
		fe) utf="d0a7"	;; # Ч U+0427
	    esac
	    fi
	    echo -n "$utf" >>$file_tmp
	    utf=""
	done
    done
    xxd -r -p $file_tmp $file_utf
    rm -f $file_tmp
done
exit
