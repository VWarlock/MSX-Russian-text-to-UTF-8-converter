		57) utf="e29480"	;; # 157 граница лёгкая горизонтальная U+2500
		58) utf="e2948c"	;; # 158 граница лёгкая вниз в направо U+250C
		59) utf="e29490"	;; # 159 граница лёгкая вниз и налево U+2510
