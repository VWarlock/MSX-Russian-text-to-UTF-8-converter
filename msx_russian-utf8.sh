		a0) utf="ceb1"		;; # A0 Греческая строчная буква альфа U+03B1
		a1) utf="ceb2"		;; # A1 Греческая строчная буква бета U+03B2
		a2) utf="ce93"		;; # A2 Греческая заглавная буква гамма U+0393
		a3) utf="cf80"		;; # A3 Греческая строчная буква пи U+03C0
		a4) utf="e28891"	;; # A4 N-ичное суммирование U+2211
		a5) utf="cf83"		;; # A5 Греческая строчная буква сигма U+03C3
		a6) utf="cebc"		;; # A6 Греческая строчная буква мю U+03BC
		a7) utf="ceb3"		;; # A7 Греческая строчная буква гамма U+03B3
		a8) utf="cea6"		;; # A8 Греческая заглавная буква фи U+03A6
		a9) utf="e29fa0"	;; # A9 Ромбовидная фигура, разделённая горизонтальной линией U+27E0
		aa) utf="cea9"		;; # AA Греческая заглавная буква омега U+03A9
		ab) utf="ceb4"		;; # AB Греческая строчная буква дельта U+03B4
		ac) utf="e2889e"	;; # AC Знак бесконечности U+221E
		ad) utf="f09d9c99"	;; # AD Математическая курсивная фи (символ) U+1D719
		ae) utf="e28888"	;; # AE Принадлежит множеству U+2208
		af) utf="e28b82"	;; # AF N-ичное пересечение U+22C2
		b0) utf="e289a1"	;; # B0 Идентичный, тождество (тройное равно) U+2261
		b1) utf="c2b1"		;; # B1 Знак плюс-минус U+00B1
		b2) utf="e289a5"	;; # B2 Больше чем или равно U+2265
		b3) utf="e289a4"	;; # B3 Меньше или равный U+2264
		b4) utf="e28ea7"	;; # B4 Левая фигурная скобка, верхний крючок U+23A7
		b5) utf="e28ead"	;; # B5 Правая фигурная скобка, нижний крючок U+23AD
		b6) utf="c3b7"		;; # B6 Знак деления U+00F7
		b7) utf="e28988"	;; # B7 Почти равный U+2248
		b8) utf="c2b0"		;; # B8 Знак градуса U+00B0
		b9) utf="e28899"	;; # B9 Оператор точка маркер списка (скалярное умножение, знак умножения) U+2219
		ba) utf="efb9a3"	;; # BA Маленький дефис-минус U+FE63
		bb) utf="e2889a"	;; # BB Квадратный корень U+221A
		bc) utf="e281bf"	;; # BC Надстрочный знак латинская строчная буква n U+207F
		bd) utf="e28282"	;; # BD Подстрочный индекс два U+2082
		be) utf="e2888e"	;; # BE Конец доказательства U+220E
		bf) utf="c2a4"		;; # BF Знак валюты U+00A4
		c0) utf="d18e"	;; # ю U+044E
		c1) utf="d0b0"	;; # а U+0430
		c2) utf="d0b1"	;; # б U+0431
		c3) utf="d186"	;; # ц U+0446
		c4) utf="d0b4"	;; # д U+0434
		c5) utf="d0b5"	;; # е U+0435
		c6) utf="d184"	;; # ф U+0444
		c7) utf="d0b3"	;; # г U+0433
		c8) utf="d185"	;; # х U+0445
		c9) utf="d0b8"	;; # и U+0438
		ca) utf="d0b9"	;; # й U+0439
