		90) utf="e29780"	;; # 90 Черный треугольник с вершиной налево U+25C0
		91) utf="e2a797"	;; # 91 Закрашенные песочные часы U+29D7
		92) utf="e2a793"	;; # 92 Закрашенная бабочка U+29D3
		93) utf="e29698"	;; # 93 Квадрант сверху слева U+2598
		94) utf="e29697"	;; # 94 Квадрант снизу справа U+2597
		95) utf="e2969D"	;; # 95 Квадрант сверху справа U+259D
		96) utf="e29696"	;; # 96 Квадрант снизу слева U+2596
